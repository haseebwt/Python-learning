# For Loops:

# A loop is used to execute a particular statement until a condition is satisfied.

# For example:
# Let's assume we have a list containing numbers 1 to 5 and we want to multiply them all by 5

list = [1, 2, 3]

for cell in list:
    multiply_by_5 = cell * 5
    print(multiply_by_5)

