# If statement - Advanced:

# pass statement:
# It is used as a placeholder for if statement. Allowing the user to leave the 'empty' under an
# if statement. If you don't add 'pass' and leave the space empty. An error will arise.

# For example:

a = 20
b = 50

if b > a:
    pass

# If I execute this if statement without the pass statament. A syntax error will occur.

